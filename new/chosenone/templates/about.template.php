	<b>What is Play By Lyrics?</b><br />
	This site enables you to type in songtexts (or 'lyrics') from songs and automatically find the music video for it. You can <b>type in
	whatever you remember of the song, and it will find it!</b> If you know the artist or song title, adding this to the search usually
	enhances the results.<br />
	Some examples:<br />
	<ul>
		<li><a href='./?lyrics=when I ruled the world'>when I ruled the world</a></li>
		<li><a href='./?lyrics=Paint black heart'>Paint black heart</a></li>
		<li><a href='./?lyrics=loosing control come on rock everybody'>loosing control come on rock everybody</a></li>
	</ul>
	<br />
	You could do it the hard way and google up the song, but I think it's rather tedious. This site:<br />
	<ul>
		<li>Is much faster than doing it yourself;</li>
		<li>Can download the mp3 with just a click; and</li>
		<li>Has some more features like auto-replay.</li>
	</ul>
